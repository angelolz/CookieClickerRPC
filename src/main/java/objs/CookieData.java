    public String getCookiesPerClick()
    {
        return cookiesPerClick;
    }

    public String getSeason()
    {
        return season;
    }

    public String getSeasonName()
    {
        return seasonName;
    }

    public String getDrops()
    {
        return drops;
    }

    public String getGoldenCookiesClicked()
    {
        return goldenCookiesClicked;
    }

    public String getGoldenCookiesMissed()
    {
        return goldenCookiesMissed;
    }

